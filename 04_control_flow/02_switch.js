// Switch Case

let month = "jan"
switch (month) {
    case "jan":
        console.log("January")
        break;

    case "feb":
        console.log("February")
        break;

    case "jan":
        console.log("March")
        break;
    
    case "April":
        console.log("April")
        break;
    
    default:
        console.log("Invalid month");
        break;
}